def get_treatment(disease_key):
    if "healthy" in disease_key.lower():
        return {
            'chemical': 'None required',
            'organic': 'Maintain regular compost/fertilizer routine',
            'dosage_timing': 'N/A',
            'preventive': 'Continue current good agricultural practices'
        }
    return smart_treatments.get(disease_key, {
        'chemical': 'Broad-spectrum fungicide/bactericide (Consult local extension)',
        'organic': 'Neem oil or Copper-based sprays',
        'dosage_timing': 'Follow product label instructions',
        'preventive': 'Ensure good airflow, practice crop rotation, remove infected debris'
    })
